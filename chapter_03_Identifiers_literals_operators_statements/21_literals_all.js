let count=29;
let negative=-27;
let zero=0;

let h=0xff; // hexadecimal
let o=0o77; // octal
let b=0b1010; // binary
let color_hex="#ff0000"; // hexadecimal color code
let color_rgb="rgb(255, 0, 0)"; // RGB color code

