console.log(negative); // Output: -27
console.log(zero); // Output: 0
console.log(h); // Output: 255
console.log(o); // Output: 63
console.log(b); // Output: 10
console.log(color_hex); // Output: #ff0000
console.log(color_rgb); // Output: rgb(255, 0, 0)


let single ='Hello'; // single quotes
let double ="World"; // double quotes

let backtick=`Hello`; // backticks

let templateLiteral=`hi ${single} ${double} ${backtick}`; // template literal
console.log(single); // Output: Hello
console.log(double);    // Output: World
console.log(backtick);  // Output: Hello
console.log(templateLiteral); // Output: hi Hello World Hello